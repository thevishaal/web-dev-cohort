/**
 * Create an object named `book` with properties: `title` = 'JavaScript Guide' and `pages` = 300. Then add a new property `author` set to 'John Doe'. Finally, change the `pages` property to 350. Log the final object.
 */

// solution

// Create your book object
const book = {
  title: "JavaScript Guide",
  pages: 300,
};
// Add the author property
book.author = "John Doe";
// Modify the pages property
book.pages = 350;
// Log the final object
console.log(book);
