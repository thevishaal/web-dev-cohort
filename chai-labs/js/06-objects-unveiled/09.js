/**
 * Create three variables: `username` = 'alice123', `email` = 'alice@example.com', and `isVerified` = true. Create an object named `account` using property shorthand (without repeating the property names). Log the account object.
 */

// solution

// Create your variables
const username = "alice123";
const email = "alice@example.com";
const isVerified = true;
// Create account object using shorthand
const account = {
  username,
  email,
  isVerified,
};
// Log the account
console.log(account);
