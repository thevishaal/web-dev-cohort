/**
 * Create four variables: `str` = 'hello', `num` = 42, `bool` = true, and `arr` = [1, 2, 3]. Use typeof to log the type of each variable in the format: 'str is string'
 */

// solution

// Create your variables
const str = "hello";
const num = 42;
const bool = true;
const arr = [1, 2, 3];
// Log the type of each variable
console.log(`str is ${typeof str}`);
console.log(`num is ${typeof num}`);
console.log(`bool is ${typeof bool}`);
console.log(`arr is ${typeof arr}`);
