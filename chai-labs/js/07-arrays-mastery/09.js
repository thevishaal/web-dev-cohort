/**
 * Create an array named `numbers` with [1, 2, 3, 4]. Use map() to create a new array `doubled` where each number is multiplied by 2. Log the doubled array.
 */

// solution

// Create your numbers array
const numbers = [1, 2, 3, 4];
// Use map to double each number
const doubled = numbers.map((n) => n * 2);
// Log the doubled array
console.log(doubled);
