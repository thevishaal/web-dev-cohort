/**
 * Create an array named `prices` with [10, 20, 30]. Use forEach() to iterate over each price and log it to the console. Each price should be logged on a separate line.
 */

//  solution

// Create your prices array
const prices = [10, 20, 30];
// Use forEach to log each price
for (const price of prices) {
  console.log(price);
}
