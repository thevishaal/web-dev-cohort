/**
 * Create a function named `add` that takes two parameters `a` and `b`, and returns their sum. Create a variable `result` that stores the return value of calling add(5, 3), then log the result.
 */

// solution

// Declare your add function
function add(a, b) {
  return a + b;
}
// Call the function and store result
const result = add(5, 3);
// Log the result
console.log(result);
