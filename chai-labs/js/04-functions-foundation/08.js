/**
 * Create a function expression (not a declaration) named `subtract` that takes two parameters and returns their difference (first minus second). Assign it to a const variable. Call it with 20 and 8, and log the result.
 */

// solution

// Create a function expression
const subtract = function (a, b) {
  return a - b;
};
// Call and log the result
console.log(subtract(20, 8));
