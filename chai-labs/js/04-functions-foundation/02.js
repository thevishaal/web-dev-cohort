/**
 * Create a function named `greetPerson` that accepts one parameter called `name`. The function should log 'Hello, [name]!' where [name] is the parameter value. Call the function with the argument 'Alice'.
 */

// solution

// Declare your function with a parameter
function greetPerson(name) {
  console.log(`Hello, ${name}!`);
}
// Call the function with 'Alice'
greetPerson("Alice");
