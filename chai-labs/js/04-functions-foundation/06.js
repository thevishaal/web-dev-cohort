/**
 * Create an arrow function named `square` that takes one parameter `n` and returns n squared (n * n). Use the implicit return syntax (no curly braces, no return keyword). Call it with 8 and log the result.
 */

// solution

// Declare your arrow function with implicit return
const square = (n) => n * n;
// Call and log the result
const result = square(8);
console.log(result);
