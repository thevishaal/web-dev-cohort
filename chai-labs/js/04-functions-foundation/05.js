/**
 * Create an arrow function named `multiply` that takes two parameters `x` and `y`, and returns their product. Use the arrow function syntax (=>). Call it with 6 and 7, and log the result.
 */

// solution

// Declare your arrow function
const multiply = (x, y) => {
  return x * y;
};
// Call and log the result
const result = multiply(6, 7);
console.log(result);
