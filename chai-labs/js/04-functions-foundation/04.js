/**
 * Create a function named `calculateArea` that takes two parameters `width` and `height`, and returns their product (area of rectangle). Call the function with arguments 10 and 5, and log the result.
 */

// solution

// Declare your calculateArea function
function calculateArea(width, height) {
  return width * height;
}
// Call and log the result
const result = calculateArea(10, 5);
console.log(result);
