/**
 * Create variables `name` with value 'Alice' and `age` with value 30. Then create a `greeting` variable using template literals that says: 'Hello, my name is Alice and I am 30 years old'
 */

// solution

// Use template literals (backticks)
const name = "Alice";
const age = 30;
// Create greeting using template literals
const greeting = `Hello, my name is ${name} and I am ${age} years old`;
